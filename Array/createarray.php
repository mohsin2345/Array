<?php
// Creating a simple indexed array
$fruits = ["apple", "banana", "cherry"];

// Display the array
echo "Fruits array:\n";
print_r($fruits);

// Creating an associative array
$colors = [
    "red" => "#FF0000",
    "green" => "#00FF00",
    "blue" => "#0000FF"
];

// Display the associative array
echo "\nColors array:\n";
print_r($colors);

// Creating a multi-dimensional array
$products = [
    ["name" => "Laptop", "price" => 1000],
    ["name" => "Phone", "price" => 500],
    ["name" => "Tablet", "price" => 300]
];

// Display the multi-dimensional array
echo "\nProducts array:\n";
print_r($products);
?>
